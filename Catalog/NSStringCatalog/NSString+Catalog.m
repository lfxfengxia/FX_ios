    NSDate *date = [NSDate dateWithTimeIntervalSince1970: time/1000];
    NSDateFormatter *dateFormat = [[NSDateFormatter alloc] init];
    [dateFormat setDateFormat:formatStr];
    return [dateFormat stringFromDate: date];
}


